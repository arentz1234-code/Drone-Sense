// Helper function to get state from coordinates (would need reverse geocoding)
export function getRegionFromState(stateCode: string): string[] {
  const regions: string[] = [];
  for (const [region, states] of Object.entries(US_REGIONS)) {
    if (states.includes(stateCode) || states.includes('ALL')) {
      regions.push(region);
    }
  }
  return regions;
}

